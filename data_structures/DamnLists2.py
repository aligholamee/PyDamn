# create a list
list1 = [1, 2, 3, 5]

# create another list
list2 = [7, 8, 9, 10]

# extend list1 with the contents of list2
list1.extend(list2)

# reverse items in list1
list1.reverse()

# remove a specific item from the list1 
list1.remove(1)

# remove and return a specific index
list1.pop([2])

# insert a new item into the list
# (ide)
list1.insert(2, 5)